export async function getStaticProps() {
  const { data } = await client.query({
    query: gql`
      query Query {
        getFilms {
          id
          title
          description
          director
          scenario
          genre
          production
          premiere
          miniature
          content
          duration
          like
          dislike
          type
          platforms
        }
      }
    `,
  });

  return {
    props: {
      data: data,
    },
  };
}

